// setInterval: it is a method in javascript which calls a function after a particular interval of time(time is given in milli seconds).
// for example 
let i=0;
setInterval(()=>{console.log("hello dada :)",i);i++;},1000);

// setTimeout: it is a method in javascript which calls the function after completion of the given time(timer is given in milli seconds).
//For example
setTimeout(()=>{console.log("bye dada");},4000);